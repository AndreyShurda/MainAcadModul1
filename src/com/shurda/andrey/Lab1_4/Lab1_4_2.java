package com.shurda.andrey.Lab1_4;

/**
 * Write a console program that prints result of each of
 * arithmetic operations (+, -, /, *, %) for two variables of primitive data types.
 */
public class Lab1_4_2 {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;

        System.out.println("a+b=" + (a + b));
        System.out.println("a-b=" + (a - b));
        System.out.println("a*b=" + a * b);
        System.out.println("a/b=" + a / b);
        System.out.println("a%b=" + a % b);
    }
}
