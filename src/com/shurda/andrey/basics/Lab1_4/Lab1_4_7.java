package com.shurda.andrey.basics.Lab1_4;

/**
 * Write a console program that prints result of Ternary operator for integer variable initialization.
 */
public class Lab1_4_7 {
    public static void main(String[] args) {
        int a = 16;
        String b = a > 0 ? "Pozitivnoe" : "Negativnoe";

        System.out.println(a > 0 ? true : false);
        System.out.println(b);
    }
}
